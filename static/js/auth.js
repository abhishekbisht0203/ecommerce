// auth.js - Handles login form, password toggle, and Google login integration

document.addEventListener('DOMContentLoaded', function () {
  const loginForm = document.getElementById('login');
  const loginBtn = document.getElementById('loginBtn');
  const spinner = document.getElementById('loginSpinner');
  const togglePwd = document.getElementById('togglePassword');

  // Password visibility toggle
  if (togglePwd) {
    togglePwd.addEventListener('click', function () {
      const pwdInput = document.getElementById('password');
      if (pwdInput.type === 'password') {
        pwdInput.type = 'text';
        this.innerHTML = '<i class="fas fa-eye-slash"></i>';
      } else {
        pwdInput.type = 'password';
        this.innerHTML = '<i class="fas fa-eye"></i>';
      }
    });
  }

  // Submit login via AJAX (axios)
  if (loginForm) {
    loginForm.addEventListener('submit', function (e) {
      e.preventDefault();
      const formData = new FormData(loginForm);
      // Show spinner
      if (spinner) spinner.classList.add('visible');
      fetch(loginForm.action, {
        method: 'POST',
        body: formData,
        // Let Django handle CSRF via the existing cookie; fetch sends cookies by default
      })
        .then(function (resp) { return resp.json(); })
        .then(function (data) {
          if (data.success) {
            Swal.fire({icon: 'success', title: 'Login successful', timer: 1500, showConfirmButton: false});
            setTimeout(() => { window.location.href = '/'; }, 1500);
          } else {
            Swal.fire({icon: 'error', title: 'Login failed', text: data.error || 'Invalid credentials'});
          }
        })
        .catch(function (err) {
          Swal.fire({icon: 'error', title: 'Error', text: err.message});
        })
        .finally(function () {
          if (spinner) spinner.classList.remove('visible');
        });
    });
  }

  // Google login button redirects to allauth URL
  const googleBtn = document.getElementById('googleLoginBtn');
  if (googleBtn) {
    googleBtn.addEventListener('click', function () {
      // URL generated by allauth provider_login_url tag
      const url = this.getAttribute('data-href');
      if (url) window.location.href = url;
    });
  }
});
